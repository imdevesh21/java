
public class switchincase {

    public static void main(String[] args) {

        int number = 2;
        switch (number) {

            case 1:
                System.out.println("case1");
            case 2:
                System.out.println("case2");
                break;
            case 3:
                System.out.println("case3");
                break;
            default:
                System.out.println("kuch nhi");

        }
    }

}
